import type { BrainrotAsset } from '../types';

function makeAsset(
  overrides: Partial<BrainrotAsset> & { id: string; name: string; ticker: string; category: BrainrotAsset['category']; startingPrice: number }
): BrainrotAsset {
  const volatility = overrides.baseVolatility ?? 0.05;
  return {
    description: '',
    rarity: 'Common',
    baseVolatility: volatility,
    currentVolatility: volatility,
    popularity: 50,
    hype: 10,
    publicTrust: 50,
    riskRating: 'Medium',
    volume: 10000,
    supply: 1000000,
    demand: 50000,
    historicalPrices: [overrides.startingPrice],
    dailyChange: 0,
    allTimeHigh: overrides.startingPrice,
    allTimeLow: overrides.startingPrice,
    momentum: 0,
    traits: [],
    unlocked: true,
    icon: '📦',
    color: '#888888',
    currentPrice: overrides.startingPrice,
    ...overrides,
  };
}

