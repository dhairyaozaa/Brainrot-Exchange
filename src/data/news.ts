export function generateNews(): NewsStory {
  const template = NEWS_TEMPLATES[Math.floor(Math.random() * NEWS_TEMPLATES.length)];
  const duration = Math.floor((template.effectStrength > 0 ? 50 : 80) + Math.random() * 100);

  return {
    id: makeNewsId(),
    headline: template.headline,
    description: template.description,
    relatedAssets: template.relatedAssets ?? [],
    relatedCategories: template.relatedCategories ?? [],
    reliability: template.reliability,
    effectStrength: template.effectStrength,
    effectDuration: duration,
    isTrue: template.isTrue,
    createdAt: 0,
    expiresAt: duration,
    active: true,
  };
}

export function generateRandomNews(): NewsStory {
  return generateNews();
}
