export function getRotterAccounts(): RotterAccount[] {
  return ROTTER_ACCOUNTS;
}

export function getRotterAccountById(id: string): RotterAccount | undefined {
  return ROTTER_ACCOUNTS.find(a => a.id === id);
}

export function generateRotterPost(availableTickers: string[]): { accountId: string; content: string } {
  const account = ROTTER_ACCOUNTS[Math.floor(Math.random() * ROTTER_ACCOUNTS.length)];
  const template = ROTTER_POST_TEMPLATES[Math.floor(Math.random() * ROTTER_POST_TEMPLATES.length)];
  
  const ticker = availableTickers[Math.floor(Math.random() * availableTickers.length)];
  const ticker2 = availableTickers[Math.floor(Math.random() * availableTickers.length)];
  
  let content = template
    .replace(/\${{TICKER}}/g, `$${ticker}`)
    .replace(/\${{TICKER2}}/g, `$${ticker2}`);
  
  return { accountId: account.id, content };
}
